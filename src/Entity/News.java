package Entity;

public class News {
    String title_n;
    String abstract_n;
    String imagepath_n;
    String date_n;
    String link_n;
    String source_n;

    public String getTitle_n() {
        return title_n;
    }

    public void setTitle_n(String title_n) {
        this.title_n = title_n;
    }

    public String getAbstract_n() {
        return abstract_n;
    }

    public void setAbstract_n(String abstract_n) {
        this.abstract_n = abstract_n;
    }

    public String getImagepath_n() {
        return imagepath_n;
    }

    public void setImagepath_n(String imagepath_n) {
        this.imagepath_n = imagepath_n;
    }

    public String getDate_n() {
        return date_n;
    }

    public void setDate_n(String date_n) {
        this.date_n = date_n;
    }

    public String getLink_n() {
        return link_n;
    }

    public void setLink_n(String link_n) {
        this.link_n = link_n;
    }

    public String getSource_n() {
        return source_n;
    }

    public void setSource_n(String source_n) {
        this.source_n = source_n;
    }
}
